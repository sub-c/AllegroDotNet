namespace SubC.AllegroDotNet.Enums;

/// <summary>
/// This enumeration holds version numbers in Allegro 5's integer format.
/// </summary>
public enum LibraryVersion : int
{
    /// <summary>
    /// Allegro version 5.2.6.
    /// </summary>
    V526 = (5 << 24) | (2 << 16) | (6 << 8) | 1,

    /// <summary>
    /// Allegro version 5.2.7.
    /// </summary>
    V527 = (5 << 24) | (2 << 16) | (7 << 8) | 1,


    /// <summary>
    /// Allegro version 5.2.8.
    /// </summary>
    V528 = (5 << 24) | (2 << 16) | (8 << 8) | 1,

    /// <summary>
    /// Allegro version 5.2.9.
    /// </summary>
    V529 = (5 << 24) | (2 << 16) | (9 << 8) | 1,

    /// <summary>
    /// Allegro version 5.2.10.
    /// </summary>
    V5210 = (5 << 24) | (2 << 16) | (10 << 8) | 1,

    /// <summary>
    /// Allegro version 5.2.11.
    /// </summary>
    V5211 = (5 << 24) | (2 << 16) | (11 << 8) | 1
}
